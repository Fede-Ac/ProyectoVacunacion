    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        if (tblVacunadores.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila");
        }else if(tblVacunadores.getSelectedRowCount() > 1){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una sola fila");
        }else{
            DefaultTableModel modelo = (DefaultTableModel) tblVacunadores.getModel(); 
            
            //cargo
            Vacunadores vacunador = new Vacunadores();
            vacunador.setCodigo(Integer.parseInt ((String) modelo.getValueAt(tblVacunadores.getSelectedRow(), 0)));
            vacunador.setNombre((String) modelo.getValueAt(tblVacunadores.getSelectedRow(), 1));
            if("Mañana".equals((String) modelo.getValueAt(tblVacunadores.getSelectedRow(), 2))){
                vacunador.setTurno(0);
            }else{
                vacunador.setTurno(1);
            }
            
            //envio
            VentanaMVacunador vmv = new VentanaMVacunador();
            vmv.toFront();
            vmv.setVisible(true);
            vmv.vacunador = vacunador;
        }        

        
    }//GEN-LAST:event_modificarActionPerformed

    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tblVacunadores.getModel();
        int codigoVacunador = Integer.parseInt ((String) modelo.getValueAt(tblVacunadores.getSelectedRow(), 0));
        try {
            FachadaLogica.borrarVacunador(codigoVacunador);
        } catch (VacunadorException ex) {
            Logger.getLogger(VentanaVacunadores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (PersistenciaException ex) {
            Logger.getLogger(VentanaVacunadores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_borrarActionPerformed


