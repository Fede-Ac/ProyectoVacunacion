    public void mostrarVacunador(){
        DefaultTableModel tVacunador = new DefaultTableModel();
        PersistenciaVacunador pvacunador = new PersistenciaVacunador();
        Vacunadores vacunadores = null;
        try {
            vacunadores = pvacunador.consultar();
        } catch (VacunadorException ex) {
            Logger.getLogger(VentanaVacunadores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (PersistenciaException ex) {
            Logger.getLogger(VentanaVacunadores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        tVacunador.addColumn("Codigo");
        tVacunador.addColumn("Nombre");
        tVacunador.addColumn("Turno");
        int i = 0;
        while (i < vacunadores.getVacunadores().size()) {
            Vacunador vacuna = new Vacunador();
            vacuna = vacunadores.getVacunadores().get(i);
            String[] fila = new String[3];
            fila[0] = String.valueOf(vacuna.getCodigo());
            fila[1] = vacuna.getNombre();
            if (vacuna.getTurno()== 0) {
                fila[2] = "Mañana";
            } else {
                fila[2] = "Tarde";
            }
            tVacunador.addRow(fila);
            i++;
        }
        this.tblVacunadores.setModel(tVacunador);
    }

