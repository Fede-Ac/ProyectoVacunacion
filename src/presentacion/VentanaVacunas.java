    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        if (tblDatos.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila");
        }else if(tblDatos.getSelectedRowCount() > 1){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una sola fila");
        }else{
            DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
            
            //convierto      
            Date date = new Date(2022-04-20); //((long) modelo.getValueAt(tblDatos.getSelectedRow(), 3));
            long d = date.getTime();
            java.sql.Date fechaVencimiento = new java.sql.Date(d);     
            
            //cargo
            Vacuna vacuna = new Vacuna();

            vacuna.setCodigo(Integer.parseInt ((String) modelo.getValueAt(tblDatos.getSelectedRow(), 0)));
            vacuna.setNombre((String) modelo.getValueAt(tblDatos.getSelectedRow(), 1));
            vacuna.setCantidadDosis(Integer.parseInt ((String)  modelo.getValueAt(tblDatos.getSelectedRow(), 2)));
            vacuna.setFechaVencimiento(fechaVencimiento);
            if ("Unidosis".equals((String) modelo.getValueAt(tblDatos.getSelectedRow(), 4))){
                vacuna.setCategoria(0);
            }else{
                vacuna.setCategoria(1);
            }

            //envio
            VentanaMVacuna vmv = new VentanaMVacuna();
            vmv.toFront();
            vmv.setVisible(true); 
            vmv.vacuna = vacuna;
        }  
    }//GEN-LAST:event_modificarActionPerformed


