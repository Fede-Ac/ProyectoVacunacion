    public void mostrarVacuna() {
        DefaultTableModel tVacuna = new DefaultTableModel();
        PersistenciaVacuna pVacunas = new PersistenciaVacuna();

        Vacunas vacunas = null;
        try {
            vacunas = pVacunas.consultar();
        } catch (VacunasException ex) {
            Logger.getLogger(VentanaVacunas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (PersistenciaException ex) {
            Logger.getLogger(VentanaVacunas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        tVacuna.addColumn("Codigo");
        tVacuna.addColumn("Nombre");
        tVacuna.addColumn("Dosis");
        tVacuna.addColumn("Vencimiento");
        tVacuna.addColumn("Categoria");
        int i = 0;
        while (i < vacunas.getVacunas().size()) {
            Vacuna vacuna = new Vacuna();
            vacuna = vacunas.getVacunas().get(i);
            String[] fila = new String[5];
            Date[] fila2 = new Date[4];
            fila[0] = String.valueOf(vacuna.getCodigo());
            fila[1] = vacuna.getNombre();
            fila[2] = String.valueOf(vacuna.getCantidadDosis());
            fila2[3] = Date.valueOf(vacuna.getFechaVencimiento().toLocalDate());
            
            if (vacuna.getCategoria() == 0) {
                fila[4] = "Unidosis";
            } else {
                fila[4] = "Multidosis";
            }
            tVacuna.addRow(fila);
            tVacuna.addRow(fila2);
            i++;
        }
        this.tblDatos.setModel(tVacuna);
    }
