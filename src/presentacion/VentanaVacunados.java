    public void mostrarVacunado() {
        DefaultTableModel tVacuna = new DefaultTableModel();
        PersistenciaVacunado pVacunados = new PersistenciaVacunado();

        Vacunados vacunados = pVacunados.consultar();
        tVacuna.addColumn("Documento");
        tVacuna.addColumn("Nacionalidad");
        tVacuna.addColumn("Direccion");
        tVacuna.addColumn("Telefono");
        tVacuna.addColumn("Email");
        tVacuna.addColumn("Fecha Nacimiento");
        tVacuna.addColumn("Vacunas Otorgadas");
        int i = 0;
        while (i < vacunados.getVacunados().size()) {
            Vacunado vacuna = new Vacunado();
            vacuna = vacunados.getVacunados().get(i);
            String[] fila = new String[7];
            fila[0] = vacuna.getDocumento();
            fila[1] = vacuna.getNacionalidad();
            fila[2] = vacuna.getDireccion();
            fila[3] = vacuna.getTelefono();
            fila[4] = vacuna.getEmail();
            fila[5] = String.valueOf(vacuna.getFechaNac());
            fila[6] = String.valueOf(vacuna.getVacunas());
            tVacuna.addRow(fila);
            i++;
        }

        this.tblVacunado.setModel(tVacuna);

    }

